class Facts
{
    static long factorial(int par)
    {
	long answer = 1;
	if (par == 0)
	    return answer;
	for (int c = 1; c <= par; c++)
	    {
		answer = answer * c;
	    }
	return answer;
    }

    public static void main(String args[])
    {
	System.out.println("n" + "       " + "n!");
	System.out.println("------------");
	for (int n = 0; n <= 6;  n++)
	    {
		System.out.println(n + "       " + factorial(n));
	    }
	return;
    }
}
